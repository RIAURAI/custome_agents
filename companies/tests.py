class InviteFlowTests(TestCase):
    def setUp(self):
        self.admin = User.objects.create_user(
            "admin", email="admin@co.com", password="StrongPass123!"
        )
        self.company = Company.objects.create(name="Acme")
        Membership.objects.create(user=self.admin, company=self.company, role=Membership.Role.OWNER)
        self.client.login(username="admin", password="StrongPass123!")

    def test_admin_can_send_invite(self):
        resp = self.client.post(reverse("companies:invite"), {
            "email": "newguy@co.com",
            "role": "member",
        })
        self.assertEqual(resp.status_code, 302)
        self.assertTrue(Invitation.objects.filter(email="newguy@co.com").exists())

    def test_member_cannot_send_invite(self):
        member = User.objects.create_user("mem", email="mem@co.com", password="StrongPass123!")
        Membership.objects.create(user=member, company=self.company, role=Membership.Role.MEMBER)
        self.client.login(username="mem", password="StrongPass123!")
        resp = self.client.post(reverse("companies:invite"), {
            "email": "sneaky@co.com",
            "role": "member",
        })
        self.assertEqual(resp.status_code, 302)
        self.assertFalse(Invitation.objects.filter(email="sneaky@co.com").exists())

    def test_accept_invite_creates_membership(self):
        inv = Invitation.objects.create(
            company=self.company,
            email="joiner@co.com",
            role=Membership.Role.MEMBER,
            invited_by=self.admin,
            expires_at=timezone.now() + timezone.timedelta(days=7),
        )
        self.client.logout()
        resp = self.client.post(reverse("companies:accept_invite", args=[inv.token]), {
            "username": "joiner",
            "password": "StrongJoinPass1!",
            "first_name": "Joe",
            "last_name": "Iner",
        })
        self.assertEqual(resp.status_code, 302)
        inv.refresh_from_db()
        self.assertEqual(inv.status, Invitation.Status.ACCEPTED)
        self.assertTrue(
            Membership.objects.filter(user__username="joiner", company=self.company).exists()
        )

    def test_expired_invite_shows_invalid(self):
        inv = Invitation.objects.create(
            company=self.company,
            email="late@co.com",
            invited_by=self.admin,
            expires_at=timezone.now() - timezone.timedelta(hours=1),
        )
        self.client.logout()
        resp = self.client.get(reverse("companies:accept_invite", args=[inv.token]))
        self.assertEqual(resp.status_code, 200)
        self.assertContains(resp, "expired")

    def test_revoke_invite(self):
        inv = Invitation.objects.create(
            company=self.company,
            email="gone@co.com",
            invited_by=self.admin,
            expires_at=timezone.now() + timezone.timedelta(days=7),
        )
        resp = self.client.post(reverse("companies:revoke_invite", args=[inv.token]))
        self.assertEqual(resp.status_code, 302)
        inv.refresh_from_db()
        self.assertEqual(inv.status, Invitation.Status.REVOKED)


