class PlatformAccessDecoratorTests(TestCase):
    """Test that platform views enforce access control for members."""

    def setUp(self):
        self.admin = User.objects.create_user(
            "admin", email="admin@co.com", password="StrongPass123!"
        )
        self.member = User.objects.create_user(
            "member", email="member@co.com", password="StrongPass123!"
        )
        self.company = Company.objects.create(name="Acme")
        self.admin_membership = Membership.objects.create(
            user=self.admin, company=self.company, role=Membership.Role.OWNER
        )
        self.member_membership = Membership.objects.create(
            user=self.member, company=self.company, role=Membership.Role.MEMBER
        )
        self.ms_integration = CompanyIntegration.objects.create(
            company=self.company, service="microsoft", status="active"
        )

    def test_admin_always_has_access(self):
        """Admins bypass PlatformAccess — they always have full access."""
        self.client.login(username="admin", password="StrongPass123!")
        # Email inbox requires microsoft view access
        resp = self.client.get(reverse("email_hub:inbox"))
        # Should get 200 (or any non-redirect), not blocked
        self.assertNotEqual(resp.status_code, 302)

    def test_member_without_access_is_blocked(self):
        """Member with no PlatformAccess is redirected."""
        self.client.login(username="member", password="StrongPass123!")
        resp = self.client.get(reverse("email_hub:inbox"))
        self.assertEqual(resp.status_code, 302)

    def test_member_with_view_access_can_view(self):
        """Member with view access can access inbox."""
        PlatformAccess.objects.create(
            membership=self.member_membership,
            integration=self.ms_integration,
            permission=PlatformAccess.Permission.VIEW,
        )
        self.client.login(username="member", password="StrongPass123!")
        resp = self.client.get(reverse("email_hub:inbox"))
        # Should not be blocked (302 to dashboard)
        # May still redirect to connect if no token, but not to dashboard
        if resp.status_code == 302:
            self.assertNotIn("dashboard", resp.url)

    def test_member_view_access_cannot_compose(self):
        """Member with view-only access is blocked from compose (needs reply)."""
        PlatformAccess.objects.create(
            membership=self.member_membership,
            integration=self.ms_integration,
            permission=PlatformAccess.Permission.VIEW,
        )
        self.client.login(username="member", password="StrongPass123!")
        resp = self.client.get(reverse("email_hub:compose"))
        self.assertEqual(resp.status_code, 302)

    def test_member_reply_access_can_compose(self):
        """Member with reply access can access compose."""
        PlatformAccess.objects.create(
            membership=self.member_membership,
            integration=self.ms_integration,
            permission=PlatformAccess.Permission.REPLY,
        )
        self.client.login(username="member", password="StrongPass123!")
        resp = self.client.get(reverse("email_hub:compose"))
        if resp.status_code == 302:
            self.assertNotIn("dashboard", resp.url)


